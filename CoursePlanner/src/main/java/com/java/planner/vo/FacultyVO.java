public class FacultyVO {
    
    String lastName;
    String firstName;
    String gradSchool;
    String degree;
    String title;
    String dyasToTeach;
    String maxLoadfall;
    String maxLoadSpring;
    String maxLoadSummer;

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getGradSchool() {
        return gradSchool;
    }

    public void setGradSchool(String gradSchool) {
        this.gradSchool = gradSchool;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDyasToTeach() {
        return dyasToTeach;
    }

    public void setDyasToTeach(String dyasToTeach) {
        this.dyasToTeach = dyasToTeach;
    }

    public String getMaxLoadfall() {
        return maxLoadfall;
    }

    public void setMaxLoadfall(String maxLoadfall) {
        this.maxLoadfall = maxLoadfall;
    }

    public String getMaxLoadSpring() {
        return maxLoadSpring;
    }

    public void setMaxLoadSpring(String maxLoadSpring) {
        this.maxLoadSpring = maxLoadSpring;
    }

    public String getMaxLoadSummer() {
        return maxLoadSummer;
    }

    public void setMaxLoadSummer(String maxLoadSummer) {
        this.maxLoadSummer = maxLoadSummer;
    }
    
}
